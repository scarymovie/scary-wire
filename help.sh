# Быстрая справка по всем командам WireGuard VPN

cat << 'EOF'
╔══════════════════════════════════════════════════════════════════════════════╗
║                    WireGuard VPN - Справка по командам                       ║
╚══════════════════════════════════════════════════════════════════════════════╝

📦 УСТАНОВКА И НАСТРОЙКА
────────────────────────────────────────────────────────────────────────────────
  sudo bash setup-wireguard.sh          Полная установка WireGuard VPN
  sudo bash quick-start.sh               Интерактивное меню управления
  sudo bash check-status.sh              Проверка статуса системы

👥 УПРАВЛЕНИЕ КЛИЕНТАМИ
────────────────────────────────────────────────────────────────────────────────
  sudo bash add-client.sh <имя> <IP>    Добавить нового клиента
  sudo bash remove-client.sh <имя>      Удалить клиента
  sudo bash list-clients.sh              Список всех клиентов
  sudo bash show-qr.sh <имя>             Показать QR код клиента

📊 МОНИТОРИНГ И ДИАГНОСТИКА
────────────────────────────────────────────────────────────────────────────────
  sudo bash monitor.sh                   Мониторинг в реальном времени
  sudo bash generate-report.sh           Генерация отчета об использовании
  sudo bash test-connection.sh           Тест соединения (на клиенте)
  sudo wg show                           Показать активные подключения
  sudo systemctl status wg-quick@wg0     Статус сервиса

💾 РЕЗЕРВНОЕ КОПИРОВАНИЕ
────────────────────────────────────────────────────────────────────────────────
  sudo bash backup.sh                    Создать резервную копию
  sudo bash restore.sh <файл>            Восстановить из резервной копии

⚙️  НАСТРОЙКА
────────────────────────────────────────────────────────────────────────────────
  sudo bash update-endpoint.sh           Обновить IP адрес сервера
  sudo bash setup-split-tunnel.sh        Настроить split-tunnel
                <имя> "<сети>"
  sudo bash setup-ru-bypass.sh           Настроить обход .ru и .рф доменов

🔧 УПРАВЛЕНИЕ СЕРВИСОМ
────────────────────────────────────────────────────────────────────────────────
  sudo systemctl start wg-quick@wg0      Запустить VPN
  sudo systemctl stop wg-quick@wg0       Остановить VPN
  sudo systemctl restart wg-quick@wg0    Перезапустить VPN
  sudo systemctl enable wg-quick@wg0     Включить автозапуск
  sudo systemctl disable wg-quick@wg0    Отключить автозапуск

📝 ЛОГИ И ОТЛАДКА
────────────────────────────────────────────────────────────────────────────────
  sudo journalctl -u wg-quick@wg0 -f     Просмотр логов в реальном времени
  sudo journalctl -u wg-quick@wg0 -n 50  Последние 50 строк логов
  ip addr show wg0                       Информация об интерфейсе
  sudo wg show wg0                       Детальная информация о туннеле

🗑️  УДАЛЕНИЕ
────────────────────────────────────────────────────────────────────────────────
  sudo bash uninstall.sh                 Полное удаление WireGuard

📚 ДОКУМЕНТАЦИЯ
────────────────────────────────────────────────────────────────────────────────
  cat README.md                          Полная документация
  cat QUICKSTART.md                      Быстрый старт
  cat INSTALL.md                         Руководство по установке
  cat FAQ.md                             Часто задаваемые вопросы
  cat SUMMARY.md                         Сводка проекта

💡 ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ
────────────────────────────────────────────────────────────────────────────────
  # Добавить клиента для ноутбука
  sudo bash add-client.sh laptop 10.0.0.5

  # Показать QR код для телефона
  sudo bash show-qr.sh mobile

  # Настроить split-tunnel (только локальная сеть через VPN)
  sudo bash setup-split-tunnel.sh client1 "10.0.0.0/24, 192.168.1.0/24"

  # Настроить обход российских доменов
  sudo bash setup-ru-bypass.sh

  # Создать резервную копию
  sudo bash backup.sh

  # Проверить статус
  sudo bash check-status.sh

