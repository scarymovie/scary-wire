# Функция для обновления конфигурации клиента
update_client_config() {
    local CLIENT_NAME=$1
    local CLIENT_CONFIG="$SERVER_DIR/${CLIENT_NAME}.conf"

    if [ ! -f "$CLIENT_CONFIG" ]; then
        echo -e "${YELLOW}Клиент $CLIENT_NAME не найден, пропускаем${NC}"
        return
    fi

    echo -e "${YELLOW}Обновление конфигурации: $CLIENT_NAME${NC}"

    # Читаем текущую конфигурацию
    CLIENT_PRIV=$(grep "PrivateKey" "$CLIENT_CONFIG" | awk '{print $3}')
    CLIENT_IP=$(grep "Address" "$CLIENT_CONFIG" | awk '{print $3}')
    CLIENT_DNS=$(grep "DNS" "$CLIENT_CONFIG" | awk '{print $3}')
    SERVER_PUB=$(grep "PublicKey" "$CLIENT_CONFIG" | tail -1 | awk '{print $3}')
    SERVER_ENDPOINT=$(grep "Endpoint" "$CLIENT_CONFIG" | awk '{print $3}')

    # Формируем список AllowedIPs (все кроме российских подсетей)
    # Начинаем с полного интернета
    ALLOWED_IPS="0.0.0.0/0"

    # Создаем новую конфигурацию
    cat > "$CLIENT_CONFIG" <<EOF
[Interface]
PrivateKey = $CLIENT_PRIV
Address = $CLIENT_IP
DNS = $CLIENT_DNS

# Скрипт для исключения российских IP из маршрутизации
PostUp = /etc/wireguard/bypass/exclude-ru-routes.sh up %i
PostDown = /etc/wireguard/bypass/exclude-ru-routes.sh down %i

[Peer]
PublicKey = $SERVER_PUB
Endpoint = $SERVER_ENDPOINT
AllowedIPs = $ALLOWED_IPS
PersistentKeepalive = 25
EOF

    echo -e "${GREEN}✓ $CLIENT_NAME обновлен${NC}"
}

# Создаем скрипт для управления маршрутами
cat > /etc/wireguard/bypass/exclude-ru-routes.sh <<'SCRIPT_EOF'
#!/bin/bash

# Скрипт для исключения российских IP из VPN маршрутизации

INTERFACE=$2
RU_NETWORKS="/etc/wireguard/bypass/ru-networks.txt"

case "$1" in
    up)
        # При поднятии интерфейса добавляем маршруты для российских IP в обход VPN
        echo "Добавление маршрутов для российских IP в обход VPN..."

        # Получаем шлюз по умолчанию (не через VPN)
        DEFAULT_GW=$(ip route | grep default | grep -v $INTERFACE | awk '{print $3}' | head -n1)
        DEFAULT_DEV=$(ip route | grep default | grep -v $INTERFACE | awk '{print $5}' | head -n1)

        if [ -z "$DEFAULT_GW" ] || [ -z "$DEFAULT_DEV" ]; then
            echo "Не удалось определить шлюз по умолчанию"
            exit 1
        fi

        # Добавляем маршруты для каждой российской подсети
        while IFS= read -r network; do
            [ -z "$network" ] && continue
            ip route add $network via $DEFAULT_GW dev $DEFAULT_DEV 2>/dev/null || true
        done < "$RU_NETWORKS"

        echo "Маршруты добавлены. Российские сайты идут в обход VPN."
        ;;

    down)
        # При отключении интерфейса удаляем маршруты
        echo "Удаление маршрутов для российских IP..."

        while IFS= read -r network; do
            [ -z "$network" ] && continue
            ip route del $network 2>/dev/null || true
        done < "$RU_NETWORKS"

        echo "Маршруты удалены."
        ;;

    *)
        echo "Использование: $0 {up|down} <interface>"
        exit 1
        ;;
esac
SCRIPT_EOF

chmod +x /etc/wireguard/bypass/exclude-ru-routes.sh

echo -e "\n${GREEN}Скрипт управления маршрутами создан${NC}\n"

# Обновляем конфигурации всех существующих клиентов
echo -e "${BLUE}Обновление конфигураций клиентов...${NC}\n"

for conf in $SERVER_DIR/*.conf; do
    if [ -f "$conf" ] && [ "$(basename $conf)" != "wg0.conf" ]; then
        CLIENT_NAME=$(basename "$conf" .conf)
        update_client_config "$CLIENT_NAME"
    fi
done

